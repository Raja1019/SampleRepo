package com.java;

public class TemperatureConverter {

	public static void main(String[] args) {
		
		double centigradeTemp , fahrenheightTemp;
		
	    centigradeTemp = 50;
		
		fahrenheightTemp=(9*centigradeTemp+160)/5; // fahrenheit to celsius temperature converter
		
		System.out.println(fahrenheightTemp);
		
	}

}
